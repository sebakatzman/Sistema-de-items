    private void btnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearActionPerformed
            
            String nombre = txtNombre.getText();
            String contrasenia = txtContrasenia.getText();
            if ((nombre.equals("")) || (contrasenia.equals(""))) {
                JOptionPane.showMessageDialog(this,"Completar bien los campos");
            }else{
                String rol = (String) boxUsuarios.getSelectedItem();
                String nombreEquipo = (String) boxEquipos.getSelectedItem();
                Equipo equipo = this.claseControl.buscarEquipo(nombreEquipo);
                Usuario nuevoUsuario = new Usuario(nombre, contrasenia, rol, equipo);
                this.claseControl.setUsuario(nuevoUsuario);
                JOptionPane.showMessageDialog(this,"Usuario creado con exito!");
                txtNombre.setText("");
                txtContrasenia.setText("");   
            }
    }//GEN-LAST:event_btnCrearActionPerformed

